public class Example2_1 {
    public static void main(String [] args){
        System.out.println("以下为java的8种数据类型示例:");
        byte a = 100; //byte: 8bit, -128~127
        System.out.println(a);
        short b = 20000; //short: 16bit, -32768~32767
        System.out.println(b);
        int c = 2_000_000_000; //int: 32bit, -2147483648~2147483647
        System.out.println(c);
        long d = 10000000000000000L; //long: 64bit, -9223372036854775808~9223372036854775807
        System.out.println(d);
        float e = 3.14F; //单精度
        System.out.println(e);
        double f = 3.1415926; //双精度
        System.out.println(f);
        char g = 71; //字符型数据
        System.out.println(g);
        boolean h = true; //布尔型
        System.out.println(h);
    }
}
